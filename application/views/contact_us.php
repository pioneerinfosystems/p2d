                                    $("#service").css('border', '1px solid red');                       
                                    error = 1;
                                 }
                                 
                                   if ($("#messContact").val() == "") {
                                    $("#messContact").css('border', '1px solid red');                       
                                    error = 1;
                                 }
                           if (error == 1) {
                        $('#errormsg').text("Please fill all mandatory fields").show();
                            
                            $('#errormsg').fadeIn('5000', function() {
                                $('#errormsg').fadeOut(10000);
                            });
                        return false;
                    }

                    if (error == 0) {
                        $("#submit").prop('disabled', true);
                        $("#loaderimg").show();
